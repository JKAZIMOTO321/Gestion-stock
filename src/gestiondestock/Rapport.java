    public void chargerProduitsDansJTable() {
        DefaultTableModel model = (DefaultTableModel) tableData.getModel();
        model.setRowCount(0); // Efface les anciennes lignes du tableau

        try (BufferedReader br = new BufferedReader(new FileReader("stock.csv"))) {
            String ligne;
            while ((ligne = br.readLine()) != null) {
                if (ligne.startsWith("ID") || ligne.trim().isEmpty()) continue; // ignorer l'entÃªte
                    String[] donnees = ligne.split(",");
                if (donnees.length >= 5) {
                    model.addRow(new Object[]{donnees[0], donnees[1], donnees[2], donnees[3], donnees[4]});
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, 
                    "Erreur de lecture du fichier stock.csv", 
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }
    }
    
